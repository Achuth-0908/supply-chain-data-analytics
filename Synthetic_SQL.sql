CREATE TABLE SUPPLIER(
    SUPPLIER_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50) NOT NULL,
    ADDRESS VARCHAR2(250) NOT NULL,
    PHONE_NO NUMBER(10) NOT NULL,
    EMAIL VARCHAR2(50) NOT NULL
);

CREATE TABLE SUPPLIER_PRODUCTS(
    SUPPLIER_ID NUMBER NOT NULL,
    PRODUCTS_SUPPLIED VARCHAR2(100) NOT NULL,
    CONSTRAINT fk_supplier FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER(SUPPLIER_ID)
);

CREATE TABLE MANUFACTURER(
    MANUFACTURER_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(50) NOT NULL,
    ADDRESS VARCHAR2(250) NOT NULL,
    PHONE_NO NUMBER(10) NOT NULL,
    EMAIL VARCHAR2(50) NOT NULL
);

CREATE TABLE INVENTORY(
    INVENTORY_ID NUMBER PRIMARY KEY,
    QUANTITY NUMBER NOT NULL,
    LAST_UPDATED DATE NOT NULL
);

CREATE TABLE PRODUCT(
    PRODUCT_ID NUMBER PRIMARY KEY,
    MANUFACTURER_ID NUMBER NOT NULL,
    INVENTORY_ID NUMBER NOT NULL,
    NAME VARCHAR2(50) NOT NULL,
    CATEGORY VARCHAR2(100) NOT NULL,
    PRICE NUMBER(10,2) NOT NULL,
    CONSTRAINT fk_manufacturer FOREIGN KEY (MANUFACTURER_ID) REFERENCES MANUFACTURER(MANUFACTURER_ID),
    CONSTRAINT fk_inventory FOREIGN KEY (INVENTORY_ID) REFERENCES INVENTORY(INVENTORY_ID)
);

CREATE TABLE CUSTOMER(
    CUSTOMER_ID NUMBER PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    ADDRESS VARCHAR2(100) NOT NULL,
    PHONE_NO NUMBER(10) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL
);

CREATE TABLE CUSTOMER_ORDERS(
    CUSTOMER_ID NUMBER PRIMARY KEY,
    ORDERS_PLACED VARCHAR2(100) NOT NULL,
    CONSTRAINT fk_customer FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID)
); 

CREATE TABLE PURCHASE_ORDER(
    PURCHASE_ORDER_ID NUMBER PRIMARY KEY,
    CUSTOMER_ID NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    ORDER_DATE DATE NOT NULL,
    CONSTRAINT fk_customer_order FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID)
);

CREATE TABLE SHIPMENT(
    SHIPMENT_ID NUMBER PRIMARY KEY,
    SHIPPED_ORDER_ID NUMBER NOT NULL,
    DISPATCH_DATE DATE NOT NULL,
    DELIVERY_DATE DATE NOT NULL,
    STATUS VARCHAR2(100) NOT NULL,
    CONSTRAINT fk_shipped_order FOREIGN KEY (SHIPPED_ORDER_ID) REFERENCES PURCHASE_ORDER(PURCHASE_ORDER_ID)
);

CREATE TABLE RETURN_ORDER(
    RORDER_ID NUMBER NOT NULL,
    RETURN_ID NUMBER PRIMARY KEY,
    RETURN_DATE DATE NOT NULL,
    REASON VARCHAR2(100) NOT NULL,
    STATUS VARCHAR2(100) NOT NULL,
    CONSTRAINT fk_return_order FOREIGN KEY (RORDER_ID) REFERENCES PURCHASE_ORDER(PURCHASE_ORDER_ID)
);

CREATE TABLE ORDERS (
    ORDER_ID NUMBER PRIMARY KEY,
    ORDER_DATE DATE NOT NULL,
    EXPECTED_DATE DATE NOT NULL,
    SUPPLIER_ID NUMBER NOT NULL,
    STATUS VARCHAR2(255) NOT NULL,
    CONSTRAINT fk_order FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIER(SUPPLIER_ID)
);

